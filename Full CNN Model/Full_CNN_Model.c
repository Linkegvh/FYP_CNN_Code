// Compute Unit
int Compute_pipeline(int weights[6], int data[5], int MUL_enable, int mul_output_control, int ALU_control, int ADDER_input_ctrl, int adder_additional_data, int RELU_Enable, int bias_enable){

    // decalre variables
    int mul_result_0, mul_result_1, mul_result_2, mul_result_3, mul_result_4, mul_result_5;
    int mul_out_0, mul_out_1, mul_out_2, mul_out_3, mul_out_4, mul_out_5;
    int adder_out;
    int comparator_out;
    int ALU_out;

    // Multiplication calculation
    mul_result_0 = weights[0] * 256;
    mul_result_1 = (MUL_enable >= 1) ? weights[1] * data[0] : 0;
    mul_result_2 = (MUL_enable >= 2) ? weights[2] * data[1] : 0;
    mul_result_3 = (MUL_enable >= 3) ? weights[3] * data[2] : 0;
    mul_result_4 = (MUL_enable >= 4) ? weights[4] * data[3] : 0;
    mul_result_5 = (MUL_enable >= 5) ? weights[5] * data[4] : 0;

    // check
    // printf("Mul result 0: %d\n", mul_result_0);
    // printf("Mul result 1: %d\n", mul_result_1);
    // printf("Mul result 2: %d\n", mul_result_2);
    // printf("Mul result 3: %d\n", mul_result_3);
    // printf("Mul result 4: %d\n", mul_result_4);
    // printf("Mul result 5: %d\n", mul_result_5);

    // Mux for multiplication output
    if (mul_output_control == 0){
        mul_out_0 = bias_enable ? mul_result_0 : 0;
        mul_out_1 = mul_result_1;
        mul_out_2 = mul_result_2;
        mul_out_3 = mul_result_3;
        mul_out_4 = mul_result_4;
        mul_out_5 = mul_result_5;
    }else{
        mul_out_0 = data[0];
        mul_out_1 = data[1];
        mul_out_2 = data[2];
        mul_out_3 = data[3];
        mul_out_4 = data[4];
        mul_out_5 = 0;
    }

    // ALU stage
    adder_out = mul_out_0 + mul_out_1 + mul_out_2 + mul_out_3 + mul_out_4 + mul_out_5;
    adder_out = adder_out / 256;
    // adder_out = (RELU_Enable) ? (adder_out < 0) ? 0 : adder_out / (256) : adder_out / 256;
    adder_out = (ADDER_input_ctrl) ? adder_out + adder_additional_data : adder_out;
    adder_out = RELU_Enable ? (adder_out < 0) ? 0 : adder_out : adder_out;

    int temp_0 = (mul_out_0 > mul_out_1) ? mul_out_0 : mul_out_1;
    int temp_1 = (mul_out_2 > mul_out_3) ? mul_out_2 : mul_out_3;
    int temp_2 = (mul_out_4 > mul_out_5) ? mul_out_4 : mul_out_5;
    int temp_3 = (temp_0 > temp_1) ? temp_0 : temp_1;
    comparator_out = (temp_3 > temp_2) ? temp_3 : temp_2;
    
    // ALU mux control
    ALU_out = (ALU_control) ? comparator_out : adder_out;

    return ALU_out;
}

